package curs5;

public class WhileExample {

	public static void main(String[] args) {

		//for
		for(int i=0; i<10; i++) {
			
			System.out.println( "FOR :" + i);
		}
		System.out.println("--------------------");
		
		//while
		int j=0;
		while(j<10) {
			
			System.out.println( "While :" + j);

			j++;
		}
	}

}
